// boolean Type

// boolean : 명사나 형용사로서의 불린(Boolean)은 조지 불(영국의 수학자)의 이름에서 따온 말  1854년에 '사고의 법칙'이라는 책을 썼던 그는 AND, OR, NOT 이라는 이진 연산자를 사용해 이진 정보를 처리할 수 있는 모델을 만들 수 있도록 해주었는데, 인간의 정신작용 또한 인식된 정보를 '참' 또는 '거짓'이라는 두 개의 환원된 정보로 처리하는 과정에서 이루어진다고 생각했다.

const foo: boolean = true;
const bar: boolean = false;
console.log(typeof foo);
console.log(typeof bar);

// null, undefined, 숫자 0은 false로 간주된다.
